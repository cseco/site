                        The term mechatronics is derived from <b>mecha</b>nics and electro<b>tronics</b> which were the two initial disciplines of mechatronics. Now it is a synergistic combination of mechanical engineering, electronics, computer engineering, telecommunications engineering, systems engineering and control engineering. <i class="icon icon-done"></i><i class="icon icon-done"></i>
